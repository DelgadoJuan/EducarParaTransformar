    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (UsuarioEntity usuario: usuarios) {
            if (usuario.getUsername().equals(jTextField1.getText()) &&
                    bCryptPasswordEncoder.matches(new String(jPasswordField1.getPassword()), usuario.getPassword())) {
                noMatch = false;
                Dashboard dashboard = new Dashboard(usuario, estudianteRepository,
                        profesorRepository, usuarioRepository, cursoRepository, pagoRepository,
                        titularRepository, materiaRepository, horarioRepository);
                break;
            }
        }
        if (noMatch) {
            JOptionPane.showMessageDialog(null, "DNI o contraseña incorrectos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


