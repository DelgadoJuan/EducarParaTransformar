    public Boletin(Long usuarioId, UsuarioRepository usuarioRepository) {
        initComponents();
        this.usuarioRepository = usuarioRepository;
        UsuarioEntity usuario = usuarioRepository.findById(usuarioId).orElseThrow();
        jTable1.getTableHeader().setBackground(new Color(32,136,203));
        
        
        if (usuario.getRol().equals(Roles.Titular)) {
            TitularEntity titular = (TitularEntity) usuario;
            estudiante = titular.getHijo();
        } else {
            estudiante = (EstudianteEntity) usuario;
        }
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        cargarTabla();
        this.setVisible(true);
    }

    private void cargarTabla() {
        if(estudiante == null || estudiante.getCurso() == null || estudiante.getCalificaciones() == null){
            JOptionPane.showMessageDialog(null, "Error al cargar la tabla, faltan datos");
        }
        else{


        for (MateriaEntity materia: estudiante.getCurso().getMaterias()) {
            String notas = "";
            for (CalificacionEntity calificacion: estudiante.getCalificaciones()) {
                if (calificacion.getMateria().getNombre().equals(materia.getNombre())) {
                    notas += calificacion.getDescripcion() + ": " + calificacion.getNota();
                }
                if (calificacion.getMateria().getNombre().equals(materia.getNombre()) && estudiante.getCalificaciones().indexOf(calificacion) < estudiante.getCalificaciones().size() - 1) {
                    notas += ", ";
                }
            }
            boletinTableModel.addRow(new Object[] {materia.getNombre(), notas});
        }
        jTable1.setModel(boletinTableModel);
    }
    }

